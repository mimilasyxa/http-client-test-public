#!/bin/bash

# Проверяем, передана ли ссылка на репозиторий
if [ -z "$1" ]; then
    echo "Использование: $0 <ссылка-на-новый-репозиторий>"
    exit 1
fi

REPO_URL="$1"

# Удаляем существующую историю Git, если она есть
if [ -d ".git" ]; then
    echo "Удаление старой истории Git..."
    rm -rf .git
fi

# Инициализируем новый репозиторий
echo "Инициализация нового репозитория..."
git init

# Добавляем все файлы в новый репозиторий
echo "Добавление файлов в репозиторий..."
git add .

# Создаем первый коммит
echo "Создание первоначального коммита..."
git commit -m "Initial commit"

# Добавляем удаленный репозиторий
echo "Добавление нового удаленного репозитория..."
git remote add origin "$REPO_URL"

# Заливаем файлы на новый репозиторий
echo "Загрузка файлов в удаленный репозиторий..."
git branch -M dev
git push -u origin dev

echo "Репозиторий успешно загружен на $REPO_URL"
